class MyPair {
    private Float l;
    private Float r;

    public MyPair(Float l, Float r) {
        this.l = l;
        this.r = r;
    }

    public Float getL() {
        return l;
    }

    public Float getR() {
        return r;
    }

    public void setL(Float l) {
        this.l = l;
    }

    public void setR(Float r) {
        this.r = r;
    }
}

